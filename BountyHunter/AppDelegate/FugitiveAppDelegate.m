#pragma mark - Application's Documents directory

// Returns the URL to the application's Documents directory.
- (NSURL *)applicationDocumentsDirectory
{
    return [[[NSFileManager defaultManager] URLsForDirectory:NSDocumentDirectory inDomains:NSUserDomainMask] lastObject];
}

#pragma mark - Copy Database to App's Documents directory

/* Copy the database file to the application's Documents directory,
   if there isn't an existing database file in the directory. */
- (void)copyDatabaseToApplicationDocumentsDirectory
{
    // Path to copy the database file to.
    NSURL *destDatabasePath = [[self applicationDocumentsDirectory] URLByAppendingPathComponent:DATABASE_FILENAME];
    
    // If there is an existing database file, do nothing.
    if ([[NSFileManager defaultManager] fileExistsAtPath:[destDatabasePath path]]) {
        return;
    }
    
    // Path to the database file to copy from.
    NSURL *sourceDatabasePath = [[NSBundle mainBundle] URLForResource:[DATABASE_FILENAME stringByDeletingPathExtension] withExtension:@"sqlite"];
    
    // Attempt to copy the database file to destination path.
    NSError * __autoreleasing error;
    BOOL success = [[NSFileManager defaultManager] copyItemAtURL:sourceDatabasePath toURL:destDatabasePath error:&error];
    if (!success) {
        NSAssert1(NO, @"Failed to copy database file to application's Documents directory."
                  " Error: '%@'", [error localizedDescription]);
    }
}

